package org.drools.testing.core.rules.model;

public class Person {

	private String name;
	private Integer age;
	
	public Person () {
		
	}
	
	public Integer getAge() {
		return age;
	}
	public void setAge(Integer age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
